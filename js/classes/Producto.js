class Producto {
    constructor(nombre, presentacion, descripcion, categoria, precio, moneda, cantidad) {
        this.nombre = nombre;
        this.presentacion = presentacion;
        this.categoria = categoria;
        this.descripcion = descripcion;
        this.precio = precio;
        this.moneda = moneda;
        this.cantidad = cantidad;
    }
}

export default Producto;